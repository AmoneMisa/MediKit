export function inferMedicineTags(name: string, activeIngredient?: string): string[] {
  const haystack = `${name} ${activeIngredient ?? ''}`.toLowerCase();
  const result: string[] = [];
  for (const [tag, keywords] of Object.entries(TAG_KEYWORDS)) {
    if (keywords.some(kw => haystack.includes(kw))) {
      result.push(tag);
    }
  }
  return result;
}
